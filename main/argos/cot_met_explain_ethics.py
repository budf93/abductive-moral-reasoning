# Backbone extraction — calls the CadiBack binary to find literals common to
# all SAT solutions. Identical mechanics to cot_met_clutrr.py.
# ---------------------------------------------------------------------------
def get_bb(file, del_sols=None, seedrun=0):
    """
    Run CadiBack on pos and neg polarity CNF files and return the backbone.
    The backbone is the set of literals that have the same truth value in ALL
    satisfying assignments — i.e. the things that MUST be true/false.

    Returns dict {'pos': [literals], 'neg': [literals]}.
    """
    bb = {'pos': [], 'neg': []}
    files = [
        '/'.join(file.split('/')[:-1]) + '/pos_' + file.split('/')[-1],
        '/'.join(file.split('/')[:-1]) + '/neg_' + file.split('/')[-1]
    ]
    for i in range(len(files)):
        cur_file = files[i]
        # Copy to tempfiles dir for isolated solver runs
        shutil.copy(
            cur_file,
            '/'.join(cur_file.split('/')[:-2]) + '/tempfiles' + str(seedrun) + '/' + cur_file.split('/')[-1]
        )

        if del_sols is not None:
            # Block previously seen solutions by adding their negations as clauses
            polarity = 'pos' if 'pos' in cur_file and 'neg' not in cur_file else 'neg'
            ds = del_sols[polarity]
            for sol in ds:
                add_clause(
                    '/'.join(cur_file.split('/')[:-2]) + '/tempfiles' + str(seedrun) + '/' + cur_file.split('/')[-1]
                )
                cf = open(
                    '/'.join(cur_file.split('/')[:-2]) + '/tempfiles' + str(seedrun) + '/' + cur_file.split('/')[-1], 'a'
                )
                write_str = '\n'
                for lit in sol:
                    write_str += str(-lit) + ' '
                cf.write(write_str)
                cf.close()

        # Run CadiBack to extract the backbone
        print('running cadiback for explain_ethics backbone extraction')
        bbone_pth = (
            '/'.join(cur_file.split('/')[:-2]) + '/tempfiles' + str(seedrun) + '/'
            + cur_file.split('/')[-1][:-4] + ".bbone"
        )
        print(f"[get_bb] Writing backbone to: {bbone_pth}")
        os.system(
            "timeout 5000 " + USER_PATH + "/main/cadiback/cadiback "
            + '/'.join(cur_file.split('/')[:-2]) + '/tempfiles' + str(seedrun) + '/'
            + cur_file.split('/')[-1] + '> ' + bbone_pth
        )

        try:
            bbone_f = open(bbone_pth, 'r')
            lines = bbone_f.readlines()
        except Exception as e:
            print(f"[get_bb] Failed to open bbone file: {e}")
            # breakpoint()
            continue
        for line in lines:
            if line.startswith('b'):
                # 'b' lines contain the backbone literals as space-separated integers
                lits = line.split(' ')[1:]
                for lit in lits:
                    lit = lit.strip()
                    if lit == '0':
                        continue
                    lit = int(lit)
                    if 'pos' in cur_file and 'neg' not in cur_file:
                        bb['pos'].append(lit)
                    elif 'neg' in cur_file:
                        bb['neg'].append(lit)

    return bb
