def rule_check(rule, contra_thresh=0.3, context_thresh=0.3, prob='', llm=None, ablate=False):
    """
    Validate an LLM-generated implication rule for plausibility and context relevance.
    Identical signature to cot_met_clutrr.rule_check(); adapted prompts for ethics.

    Returns (accept: bool, scores: [contra_score, context_score])
    """
    if ablate:
        # Ablation mode: skip rule filtering, accept everything
        print('ablating rulecheck')
        return True, [0, 0]

    # Is the rule itself logically plausible? (ethical self-consistency check)
    plausibility_fs = (
        'Does the following rule seem morally true?\n'
        'Rule: If someone physically harms an animal then the norm of care is violated.\n'
        'Answer: \\box{ Yes } this seems morally true.\n'
        'Does the following rule seem morally true?\n'
        'Rule: If someone performs kindness then it violates the norm of loyalty.\n'
        'Answer: \\box{ No } this does not seem morally true.\n'
    )
    a = llm.yn([plausibility_fs + 'Does the following rule seem morally true?\nRule: ' + rule + '\nAnswer: \\box{ '])[0]

    # Is the rule relevant to the specific ethical context?
    b = llm.yn([
        'Here are some facts:\n' + prob.get('context', '') +
        '\nExplanation: ' + prob.get('explanation', '') +
        '\nDoes the following rule seem contextually relevant?\nRule: ' + rule +
        '\nAnswer "Yes" or "No": '
    ])[0]

    if a < contra_thresh:
        print(f'[rule_check] rejected by plausibility: {rule} score={a}')
        return False, [a, b]
    if b < context_thresh:
        print(f'[rule_check] rejected by context: {rule} score={b}')
        return False, [a, b]
    return True, [a, b]
# <<< [ExplainEthics Adaptation] END: rule_check for ethics


