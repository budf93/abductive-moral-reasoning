def add_clause(file):
    """
    Increment the clause count in the DIMACS header by 1.
    Called before appending a new blocking clause to a .cnf file.
    """
    f = open(file, 'r')
    lines = f.readlines()
    writestr = ''
    for line in lines:
        if line.startswith('p cnf'):
            num_var, num_clause = line.split(' ')[2], line.split(' ')[3]
            writestr += 'p cnf ' + str(num_var) + ' ' + str(int(num_clause) + 1) + '\n'
        else:
            writestr += line
    f.close()
    f = open(file, 'w')
    f.write(writestr)
    f.close()


